//REVESER STRING
function reverse(str){ 
    return str.split("").reverse().join("")
}
console.log(reverse('diego'))

//ADD (n) NUMBERS
function add(n){
    return n * (n + 1)  / 2
}
console.log(add(2))

//COUNT CHARACTERS
let countCharacters = (str)=>{
    return str.length
}
console.log(countCharacters('Eliana'))


